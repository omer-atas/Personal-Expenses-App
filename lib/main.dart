  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Personel Expenses',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        // Choosing the specific color combination - Identify a primary swatch
        primarySwatch: Colors.purple,
